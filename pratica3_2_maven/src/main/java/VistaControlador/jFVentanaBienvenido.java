    private void jMAbrirFicheroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMAbrirFicheroActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Solo archivos de texto", "txt");
        fileChooser.setFileFilter(filter);
        int seleccion = fileChooser.showOpenDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File fichero = fileChooser.getSelectedFile();
            mostrarContenidoFichero(fichero);
        }
    }//GEN-LAST:event_jMAbrirFicheroActionPerformed

    private void jMGuardarFicheroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMGuardarFicheroActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showSaveDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File fichero = fileChooser.getSelectedFile();
            //con el metodo "endsWith" comprobamos que el texto a guardar acabe en .txt devolviendo un boolean
            if (!fichero.getName().endsWith(".txt")) {
                // si no finaliza con .txt se le añade
                fichero = new File(fichero.getParentFile(), fichero.getName() + ".txt");
            }
            guardarContenidoEnFichero(fichero);
        }
    }//GEN-LAST:event_jMGuardarFicheroActionPerformed

