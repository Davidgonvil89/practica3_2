    private void mostrarContenidoFichero(File fichero) {
        try {
            BufferedReader br = new BufferedReader(new FileReader(fichero));
            String linea;
            jTMostrar.setText(""); // Limpiamos el JTextArea antes de cargar el nuevo contenido
            while ((linea = br.readLine()) != null) {
                jTMostrar.append(linea + "\n"); // Agregamos cada línea al JTextArea
            }
            br.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void guardarContenidoEnFichero(File fichero) {
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter(fichero));
            bw.write(jTMostrar.getText()); // Escribimos el contenido del JTextArea en el fichero
            bw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

